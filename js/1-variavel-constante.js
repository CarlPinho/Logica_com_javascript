/*
    var
    Variaveis declaradas com var tem escopo global ou de função
    Podem ser atualizadas e declaradas no novamente dentro do seu escopo
    Existem desde o começo na linguagem javascript


*/


var variavelVar = "Carlos"
console.log(variavelVar);

var variavelVar = "Maria"
console.log(variavelVar);




/*

let
Variaveis declaradas com let tem escopo em bloco 
Podem ser atualizadas. mas não declarada novamnete
Forem criadas posteriomente para evitar confunsão


*/
let variavelLet = 19
console.log(variavelLet);

variavelLet = 10


